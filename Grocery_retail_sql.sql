-- 3. On which dates were the most orders placed?

SELECT
    order_date,
    COUNT(ord_id) AS total_orders
FROM orders
GROUP BY order_date
HAVING COUNT(ord_id) = (
    SELECT MAX(order_count)
    FROM (
        SELECT COUNT(*) AS order_count
        FROM orders
        GROUP BY order_date
    ) AS temp
);

