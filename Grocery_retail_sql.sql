SELECT
    YEAR(STR_TO_DATE(o.order_date, '%m/%d/%Y')) AS year,
    MONTH(STR_TO_DATE(o.order_date, '%m/%d/%Y')) AS month,
    COUNT(DISTINCT o.ord_id) AS order_volume,
    SUM(od.total_price) AS total_revenue
FROM orders o
JOIN order_details od
ON o.ord_id = od.ord_id
GROUP BY
    YEAR(STR_TO_DATE(o.order_date, '%m/%d/%Y')),
    MONTH(STR_TO_DATE(o.order_date, '%m/%d/%Y'))
ORDER BY year, month;

-- 5. How do order patterns vary across weekdays and weekends?

SELECT
    CASE
        WHEN DAYOFWEEK(STR_TO_DATE(order_date, '%m/%d/%Y')) IN (1, 7)
        THEN 'Weekend'
        ELSE 'Weekday'
    END AS day_type,
    COUNT(ord_id) AS total_orders
FROM orders
GROUP BY day_type;


